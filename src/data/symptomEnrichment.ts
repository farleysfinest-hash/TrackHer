export function resolvePhasePeak(
  key: string,
  category: SymptomCategory,
  isMRSCore: boolean,
): StrawStageCode[] {
  if (SYMPTOM_PHASE_PEAK_MAP[key]) return SYMPTOM_PHASE_PEAK_MAP[key];
  if (isMRSCore) return ALL_TRANSITION;
  return CATEGORY_DEFAULT_PHASE_PEAK[category];
}

export function resolveTier(isMRSCore: boolean): number {
  return isMRSCore ? 1 : 2;
}
